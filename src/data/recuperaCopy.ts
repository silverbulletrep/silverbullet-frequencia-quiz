/**
 * Map problema_principal to pain label
 */
export const PAIN_MAP: Record<string, string> = {
    financeiro: 'Escassez Financeira',
    amor: 'Escassez Amorosa',
    relacionamento: 'Escassez Amorosa',
    saude: 'Saúde Danificada',
    espiritualidade: 'Espírito Danificado',
}

export const PAIN_FALLBACK = 'Escassez Energética'

/**
 * Get the pain label from problema_principal value
 */
export function getPainLabel(problemaPrincipal?: string | string[]): string {
    if (!problemaPrincipal) return PAIN_FALLBACK

    const key = Array.isArray(problemaPrincipal)
        ? problemaPrincipal[0]
        : problemaPrincipal

    const normalized = (key || '').toLowerCase().trim()
    return PAIN_MAP[normalized] || PAIN_FALLBACK
}
